#include <iostream>
#include "Main.h"
using namespace std;

int summ(int x, int y)//сложение
{
    return x + y;
}
int subtract(int x, int y)//вычитание
{
    return x - y;
}
int multi(int x, int y)//умножение
{
    return x * y;
}
int division(int x, int y)//деление
{
    return x / y;
}
int exponentiation(int x, int y) //степень
{
    return pow(x,y);
}
